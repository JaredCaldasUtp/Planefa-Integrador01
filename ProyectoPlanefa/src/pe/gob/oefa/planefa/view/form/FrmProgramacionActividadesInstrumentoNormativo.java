    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try {
            
            if(!this.validarFormulario()) return;
                        
            FuncionNormada funcionNormada = (FuncionNormada)this.planefa.getActividadesInstrumentoNormativo().listarFunciones().get(this.cmbFuncionNormada.getSelectedIndex()-1);            
            ActividadInstrumentoNormativo registro = new ActividadInstrumentoNormativo(funcionNormada, this.txtInstrumentoNormativo.getText(), (String)this.cmbMes.getSelectedItem());
            registro.setPresupuestoAnual(PlanefaUtils.convertirADouble(this.txtPresupuesto));
            
            if(editar){
                registro.setCorrelativo(this.actividad.getCorrelativo());
                registro.setCodigo(this.actividad.getCodigo());
                this.planefa.getActividadesInstrumentoNormativo().actualizar((ActividadInstrumentoNormativo)registro.clone());
            }else{
                this.planefa.getActividadesInstrumentoNormativo().agregar((ActividadInstrumentoNormativo)registro.clone());
            }
            //MongoDatabaseCliente.getColeccion("planefa")
            this.formPlanefa.mostrarTabla();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        this.setVisible(false);
    }//GEN-LAST:event_btnAceptarActionPerformed

