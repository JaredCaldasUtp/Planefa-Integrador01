    public boolean validarFormulario(){
        if(this.cmbFuncionNormada.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, "Seleccione función normada", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (this.txtInstrumentoNormativo.getText() == null || this.txtInstrumentoNormativo.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese instrumento normativo", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
         if(this.cmbMes.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, "Seleccione mes", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if(this.txtPresupuesto.getText()==null || this.txtPresupuesto.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Ingrese presupuesto anual", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }
        
